<div class="container">
    <h1 class='text-center font-bold text-blue-700 text-[3.75rem]'>Types of Exoplanets</h1>
    <div class="introduction text-[1.1rem]">

        <p>
            The planets beyond our solar system are called <b>“exoplanets,”</b> and they come in a wide variety of sizes, from <b>gas giants</b> larger than Jupiter to small, <b>rocky planets</b> about as big around as Earth or Mars. They can be hot enough to boil metal or locked in deep freeze. They can orbit their stars so tightly that a “year” lasts only a few days; they can orbit <b>two suns</b> at once. Some exoplanets are <b>sunless rogues</b>, wandering through the galaxy in permanent darkness. 
            <br/>
            Each planet type varies in interior and exterior appearance depending on their <b>composition</b>.
        </p>
    </div>
<div class="point">

    <h2>1. Gas giants</h2>
    <p>These are planets the size of Saturn or Jupiter, the largest planet in our solar system, or much, much larger.
        More variety is hidden within these broad categories. Hot Jupiters, for instance, were among the first planet types found – gas giants orbiting so closely to their stars that their temperatures soar into the thousands of degrees (Fahrenheit or Celsius).</p>

</div>
<div class="point">

    <h2>2. Neptunian planets</h2>
    <p>These are similar in size to Neptune or Uranus in our solar system. They likely have a mixture of interior compositions, but all will have hydrogen and helium-dominated outer atmospheres and rocky cores. We’re also discovering mini-Neptunes, planets smaller than Neptune and bigger than Earth. No planets of this size or type exist in our solar system.</p>
    
</div>
<div class="point">

    <h2>3. Super Earths</h2>
    <p>Theseare typically terrestrial planets that may or may not have atmospheres. They are more massive than Earth, but lighter than Neptune.</p>

</div>
<div class="point">
      
    <h2>4. Terrestrial planets</h2>
    <p>These are Earth sized and smaller, composed of rock, silicate, water or carbon. Further investigation will determine whether some of them possess atmospheres, oceans or other signs of habitability.</p>

</div>
</div>

